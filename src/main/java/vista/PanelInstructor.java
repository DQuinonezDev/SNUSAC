    private void btnActualizarNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarNotaActionPerformed
        // Obtiene los datos desde los campos de la interfaz
        String codigoCurso = txtCodigoCurso.getText().trim();
        String codigoSeccion = txtCodigoSeccion.getText().trim();
        String codigoEstudiante = txtCodigoEstudiante.getText().trim();
        String ponderacionTexto = txtPonderacion.getText().trim();
        String notaTexto = txtNota.getText().trim();
        String fecha = txtFecha.getText().trim();

        // Valida que ningun campo este vacio
        if (codigoCurso.isEmpty() || codigoSeccion.isEmpty() || codigoEstudiante.isEmpty()
                || ponderacionTexto.isEmpty() || notaTexto.isEmpty() || fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios para actualizar.");
            return;
        }

        // Valida que la ponderacion sea un numero decimal
        double ponderacion;
        try {
            ponderacion = Double.parseDouble(ponderacionTexto);
            if (ponderacion <= 0) {
                JOptionPane.showMessageDialog(this, "La ponderacion debe ser mayor a 0.");
                return;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El campo Ponderacion debe ser un numero.");
            return;
        }

        // Valida que la nota sea un numero decimal en rango valido
        double nota;
        try {
            nota = Double.parseDouble(notaTexto);
            if (nota < 0 || nota > 100) {
                JOptionPane.showMessageDialog(this, "La nota debe estar entre 0 y 100.");
                return;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El campo Nota debe ser un numero.");
            return;
        }

        // Llama al controlador para actualizar la nota
        boolean actualizado = ventanaPrincipal.getControladorNotas()
                .actualizarNota(codigoCurso, codigoSeccion, codigoEstudiante, ponderacion, nota, fecha);

        // Si se actualizo correctamente
        if (actualizado) {
            JOptionPane.showMessageDialog(this, "Nota actualizada correctamente.");

            // Recarga la tabla con los datos actualizados
            llenarTablaNotas();

            // Limpia los campos del formulario
            limpiarCamposNotas();

            // Guarda los cambios en archivo
            ventanaPrincipal.getControlador().guardarSistema();

        } else {
            JOptionPane.showMessageDialog(this, "No se encontro la nota a actualizar.");
        }
    }//GEN-LAST:event_btnActualizarNotaActionPerformed

