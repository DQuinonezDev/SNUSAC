    // Abre un selector de archivos para elegir un CSV e importar notas
    private void cargarCsvNotas() {
        JFileChooser selector = new JFileChooser();
        selector.setDialogTitle("Seleccionar archivo CSV de notas");
        int resultado = selector.showOpenDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = selector.getSelectedFile();
            // El controlador muestra el mensaje de exito o error internamente
            ventanaPrincipal.getControladorNotas().cargarNotasCSV(archivo.getAbsolutePath());
            llenarTablaNotas();
            ventanaPrincipal.getControlador().guardarSistema();
        }
    }

    // Abre un selector para elegir donde guardar el CSV exportado
    private void exportarCsvNotas() {
        JFileChooser selector = new JFileChooser();
        selector.setDialogTitle("Guardar archivo CSV de notas");
        selector.setSelectedFile(new File("notas_exportadas.csv"));
        int resultado = selector.showSaveDialog(this);

        if (resultado == JFileChooser.APPROVE_OPTION) {
            File archivo = selector.getSelectedFile();
            String reporte = ventanaPrincipal.getControladorNotas().exportarNotasCSV(archivo.getAbsolutePath());
            JOptionPane.showMessageDialog(this, reporte, "Resultado de exportacion", JOptionPane.INFORMATION_MESSAGE);
        }
    }

